    private void tabelaPessoasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaPessoasMouseClicked
        Botoes(true,true,true,false,false);
        int index = tabelaPessoas.getSelectedRow();
        if(index >= 0 && index < modelo.getRowCount()) {
            String temp[] = modelo.getPerson(index);
            campoNome.setText(temp[0]);
            campoSobrenome.setText(temp[1]);
        }
    }//GEN-LAST:event_tabelaPessoasMouseClicked

