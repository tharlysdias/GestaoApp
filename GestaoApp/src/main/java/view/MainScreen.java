    private void botaoSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botaoSalvarMouseClicked
        
        if (PersonController.SavePerson(campoNome.getText(),campoSobrenome.getText())) {
            this.LoadTable();
            JOptionPane.showMessageDialog(this, "Pessoa salva com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar a pessoa!");
        }
        
        Botoes(true,false,false,false,false);
    }//GEN-LAST:event_botaoSalvarMouseClicked

